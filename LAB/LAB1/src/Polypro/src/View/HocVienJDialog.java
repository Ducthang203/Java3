    void TimKiem() {
        String TimKien = txtTimKiem.getText().trim();
        List<NguoiHoc> ls = nhdao.selectAll();
        List<NguoiHoc> KetQuaTimKiem = new ArrayList<>();
        for (NguoiHoc nh : ls) {
            if (nh.getMaNguoiHoc().contains(TimKien) || nh.getHoTen().contains(TimKien) || nh.getDienThoai().contains(TimKien) || nh.getEmail().contains(TimKien)) {
                KetQuaTimKiem.add(nh);
            }
        }
        if (KetQuaTimKiem != null) {
            DefaultTableModel model = (DefaultTableModel) tbNguoiHoc.getModel();
            model.setRowCount(0);
            NguoiHocDAO dao = new NguoiHocDAO();
            for (NguoiHoc nh : KetQuaTimKiem) {
                Object[] rowdata = {nh.getMaNguoiHoc(), nh.getHoTen(), nh.getNgaySinh(), nh.isGioiTinh() ? "Nam" : " Nu", nh.getDienThoai(), nh.getEmail()};
                model.addRow(rowdata);
            }
        } else {
            fillTableNguoiHoc();
        }
    }

