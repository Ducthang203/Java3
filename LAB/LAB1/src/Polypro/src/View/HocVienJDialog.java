    public void fillTableNguoiHoc() {
        DefaultTableModel model = (DefaultTableModel) tbNguoiHoc.getModel();
        model.setRowCount(0);
        try {
            KhoaHoc khoaHoc = (KhoaHoc) cboKhoaHoc.getSelectedItem();
            if (khoaHoc != null) {
                System.out.println("MAKH " + khoaHoc.getMaKhoaHoc());
                String keyword = txtTimKiem.getText().trim();
                List<NguoiHoc> ls = nhdao.selectNotInCount(khoaHoc.getMaKhoaHoc(), keyword);
                for (NguoiHoc nh : ls) {
                    Object[] rowdata = {nh.getMaNguoiHoc(), nh.getHoTen(), nh.getNgaySinh(), nh.isGioiTinh() ? "Nam" : " Nu", nh.getDienThoai(), nh.getEmail()};
                    model.addRow(rowdata);
                }
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Loi Truy Van Du Lieu Ham fillTableNguoiHoc ");
        }
    }

    public void fillComboBoxChuyenDe() {
        DefaultComboBoxModel model = (DefaultComboBoxModel) cboChuyenDe.getModel();
        model.removeAllElements();
        try {
            List<ChuyenDe> ls = cddao.selectAll();
            for (ChuyenDe cd : ls) {
                model.addElement(cd);
            }
            fillComboBoxKhoaHoc();

        } catch (Exception e) {
            MsgBox.alert(this, "Loi Truy Van Du Lieu fillComboBoxChuyenDe");
        }
    }

    public void fillComboBoxKhoaHoc() {
        DefaultComboBoxModel model = (DefaultComboBoxModel) cboKhoaHoc.getModel();
        model.removeAllElements();
        try {
            ChuyenDe chuyenDe = (ChuyenDe) cboChuyenDe.getSelectedItem();
            if (chuyenDe != null) {
                List<KhoaHoc> ls = khdao.selectByChuyenDe(chuyenDe.getMaChuyenDe());
                for (KhoaHoc kh : ls) {
                    model.addElement(kh);
                }
            }
            fillTableHocVien();
        } catch (Exception e) {
            e.printStackTrace();
            MsgBox.alert(this, "Loi Truy Van Du Lieu fillComboBoxKhoaHoc");
        }
    }

    public void fillTableHocVien() {
        DefaultTableModel model = (DefaultTableModel) tbHocVien.getModel();
        model.setRowCount(0);
        try {
            KhoaHoc kh = (KhoaHoc) cboKhoaHoc.getSelectedItem();
            if (kh != null) {
                List<HocVien> ls = hvdao.selectByKhoaHoc(kh.getMaKhoaHoc());
                System.out.println("MaKH " + kh.getMaKhoaHoc());
                System.out.println("size " + ls.size());
                for (int i = 0; i < ls.size(); i++) {
                    HocVien hv = ls.get(i);
                    String hoTen = nhdao.selectById(hv.getMaNguoiHoc()).getHoTen();
                    Object[] rowdata = {i + 1, hv.getMaHocVien(), hv.getMaNguoiHoc(), hoTen, hv.getDiem()};
                    model.addRow(rowdata);
                }
            }
            fillTableNguoiHoc();
        } catch (Exception e) {
            MsgBox.alert(this, "Loi Truy Van Du Lieu fillTableHocVien");
        }
    }

