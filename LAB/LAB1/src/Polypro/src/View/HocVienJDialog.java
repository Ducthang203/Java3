    public void addHocVien() {
        KhoaHoc khoaHoc = (KhoaHoc) cboKhoaHoc.getSelectedItem();
        int[] selectedRows = tbNguoiHoc.getSelectedRows();
        int MaxMaHV = hvdao.getMaxMaHV();
        for (int selectedRow : selectedRows) {
            HocVien hv = new HocVien();
            hv.setMaKhoaHoc(khoaHoc.getMaKhoaHoc());
            hv.setDiem(0);
            hv.setMaHocVien(MaxMaHV + 1);
            hv.setMaNguoiHoc((String) tbNguoiHoc.getValueAt(selectedRow, 0));
            System.out.println("=> " + hv.getMaKhoaHoc() + "-" + hv.getMaNguoiHoc() + "-" + hv.getDiem());
            hvdao.insert(hv);
        }
        fillTableHocVien();
        tabs.setSelectedIndex(0);
    }

    public void removeHocVien() {
        if (!Auth.isManager()) {
            MsgBox.alert(this, "ban khong co quyen de xoa hoc vien");
        } else {
            if (MsgBox.confirm(this, "ban muon xoa cac hoc vien da duoc chon")) {
                for (int row : tbHocVien.getSelectedRows()) {
                    int MaHV = (Integer) tbHocVien.getValueAt(row, 1);
                    hvdao.delete(MaHV);
                }
                fillTableHocVien();
            }
        }
    }

